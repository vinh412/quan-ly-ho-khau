package com.example.quanly.models;

public class HoKhau {
    public String soHoKhau;
    public String hoTenChuHo;
    public String soNha;
    public String duong;
    public String xa;
    public String huyen;

    public HoKhau(String soHoKhau, String hoTenChuHo, String soNha, String duong, String xa, String huyen){
        this.soHoKhau = soHoKhau;
        this.hoTenChuHo = hoTenChuHo;
        this.soNha = soNha;
        this.duong = duong;
        this.xa = xa;
        this.huyen = huyen;
    }
}

